void Oled_DLY_ms(uint16 ms)
{                         
  uint16 a;
  while(ms)
  {
    a=1335;
    while(a--);
    ms--;
  }
  return;
}

void Oled_WrDat(uint8 data)
{
  uint8 i=8;
  //Oled_CS(0);;
  Oled_DC(1);;
  Oled_SCL(0);;   ;
  while(i--)
  {
    if(data&1){
      Oled_SDA(1);asm("nop");asm("nop");
    }
    else{
      Oled_SDA(0);asm("nop");asm("nop");
    }
    Oled_SCL(1); 
    asm("nop");asm("nop");asm("nop");asm("nop");        
    Oled_SCL(0);; 
    asm("nop");asm("nop");
    data>>=1;;    
  }
  //Oled_CS=1;
}

void Oled_WrCmd(uint8 cmd)
{
  uint8 i=8;
  
  //Oled_CS(0);;
  Oled_DC(0);;
  Oled_SCL(0);;
  //asm("nop");   
  while(i--)
  {
    if(cmd&0x80){Oled_SDA(1);;}
    else{Oled_SDA(0);;}
    Oled_SCL(1);;
    asm("nop");asm("nop");     
    Oled_SCL(0);;    
    cmd<<=1;;   
  }
  //Oled_CS=1;
}
void Oled_Set_Pos(uint8 x, uint8 y)
{ 
  uint8 xt,msb,lsb;
  //Oled_WrCmd(0xb0+y);
  //Oled_WrCmd(((x&0xf0)>>4)|0x10);
  //Oled_WrCmd((x&0x0f)|0x01); 
  xt = 1 + x * 6;
  lsb = xt % 16;
  msb = xt / 16 + 0x10;
  Oled_WrCmd(0xb0 + y);
  Oled_WrCmd(lsb);
  Oled_WrCmd(msb); 
} 

// --- Hardware Interface ---

void Oled_SCL(u8 x){                     //d0
  if(x)
    PTC->PSOR |= 1<<15;
  else
    PTC->PCOR |= 1<<15;
}
void Oled_SDA(u8 x){                             //d1
  if(x)
    PTC->PSOR |= 1<<14;
  else
    PTC->PCOR |= 1<<14;
}
void Oled_RST(u8 x){                     //res
  if(x)
    PTC->PSOR |= 1<<13;
  else
    PTC->PCOR |= 1<<13;
}
void Oled_DC(u8 x){                      //dc
  if(x)
    PTC->PSOR |= 1<<12;
  else
    PTC->PCOR |= 1<<12;
}


