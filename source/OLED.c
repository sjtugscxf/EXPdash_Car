void Oled_Putstr(u8 y,u8 x,u8 ch[])
{
  uint8 c=0,i=0,j=0,t=0; 
  while (ch[t]!='\0')
    t++;
  Oled_Set_Pos(21-x-t,7-y);
  for(j=0;j<t;j++){   
    c = ch[t-1-j]-32;
    for(i=0;i<6;i++)     
      Oled_WrDat(F6x8[c][5-i]);  
  }
}
