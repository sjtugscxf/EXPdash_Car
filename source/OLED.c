int Oled_Printf(u8 y, u8 x, u8 * format, ... )
{
	char buf[128];
	int length;
	va_list arg_list;

	va_start( arg_list, format );
	length = vsprintf( buf, format, arg_list );
	va_end( arg_list );

	Oled_Putstr(y, x, buf);

	return length;
}
void Oled_Putnum(u8 x,u8 y,s16 c){      
  uint8 cha[7],temp,sig=0;
  
  if (c<0){
    sig=1;
    c = -c;
  }
  
  cha[0]=' ';
  
  temp=c/10000;
  if(temp==0) cha[1]=' ';
  else cha[1]=temp+48;
  
  temp=c%10000/1000;
  if(temp==0 && cha[1]==' ') cha[2]=' ';
  else cha[2]=temp+48;
  
  temp=c%1000/100;
  if(temp==0 && cha[2]==' ') cha[3]=' ';
  else cha[3]=temp+48;
  
  temp=c%100/10;
  if(temp==0 && cha[3]==' ') cha[4]=' ';
  else cha[4]=temp+48; 
  
  temp=c%10;
  cha[5]=temp+48;  
  cha[6]='\0';
  if(sig){
    temp=6;
    while(cha[temp]!=' ')
      temp--;
    cha[temp]='-';
  }
  Oled_Putstr(x,y+1,cha);
}



void Oled_Clear(void)
{
	uint8 y,x;	
	for(y=0;y<8;y++)
	{
		Oled_WrCmd(0xb0+y);
		Oled_WrCmd(0x01);
		Oled_WrCmd(0x10); 
		for(x=0;x<X_WIDTH;x++)
			Oled_WrDat(0);
	}
}

void Oled_Setxy(u8 x, u8 y)     //OLED 设置坐标  
{   
  Oled_WrCmd(0xb0+y);  
  Oled_WrCmd(((x&0xf0)>>4)|0x10);  
  Oled_WrCmd((x&0x0f)|0x01);   
}   

